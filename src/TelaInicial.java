    private void iniciar(){
           try {
            // TODO add your handling code here:
            Statement st = con.createStatement(); 
            r = st.executeQuery("SELECT CODIGOA, NOMEA, ENDERECOA FROM ALUNO");
            String conteudo[] = new String[3];
            
            DefaultTableModel dm = new DefaultTableModel(); 
            dm.addColumn("Codigo:");
            dm.addColumn("Nome:");
            dm.addColumn("Endereço:");
            if(r.next()){                
                conteudo[0] = r.getString("CODIGOA");
                conteudo[1] = r.getString("NOMEA");
                conteudo[2] = r.getString("ENDERECOA");
                dm.addRow(conteudo); 
            campoCodigo.setText(r.getString("CODIGOA"));
            campoNome.setText(r.getString("NOMEA"));
            campoEnd.setText(r.getString("ENDERECOA"));
            
                
            }
            
            while(r.next()){                
                conteudo[0] = r.getString("CODIGOA");
                conteudo[1] = r.getString("NOMEA");
                conteudo[2] = r.getString("ENDERECOA");
                dm.addRow(conteudo);                
            }
            jTable1.setModel(dm);
            
        
            
            
        } catch (Exception ex) {
            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
