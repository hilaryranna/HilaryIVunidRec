 private void adicionarAluno() {
        
        String sql;
        
        try {
            
            if (con == null)
                con = new ConexaoBD().getConnection();
           
            
            
            
             sql = "INSERT INTO ALUNO (CODIGOA, NOMEA, ENDERECOA) "
                    + "VALUES (?, ?, ?)";
            
            p = con.prepareStatement(sql);
            p.setInt(1, Integer.parseInt(campoCodigo.getText()));
            p.setString(2, campoNome.getText());
            p.setString(3, campoEnd.getText());           
            p.executeUpdate();
            
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex);
        } finally {
            try {
                p.close();
                r.close();
            } catch (SQLException e) {
                System.out.println("Erro: "+e);
            }
        }
        
    }
    
   private void editarAluno() {
        String sql = "UPDATE ALUNO SET "
                    + "NOMEA = ?, "
                    + "ENDERECOA = ? "
                    + "WHERE CODIGOA = ?";
        
        
        try {
            
            if (con == null)
                con = new ConexaoBD().getConnection();
            
            p = con.prepareStatement(sql);
            p.setInt(3, Integer.valueOf(campoCodigo.getText()));
            p.setString(1, campoNome.getText());
            p.setString(2, campoEnd.getText());
            
            p.executeUpdate();
            
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex);
        }
    
    }
    
   private void excluirAluno() {
        String sql = "DELETE FROM ALUNO "
                    + "WHERE CODIGOA = ?";
        
        try {
            
            if (con == null)
                con = new ConexaoBD().getConnection();
            
            p = con.prepareStatement(sql);
            p.setInt(1, Integer.valueOf(campoCodigo.getText()));
            
            p.executeUpdate();
            
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex);
        }
        
    }
    
    
    
